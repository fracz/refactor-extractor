<?php

namespace Wallabag\CoreBundle\Helper;

class Content
{
    private $title;

    private $body;

    public function __constructor() {

    }

    public function getTitle()
    {
        return $this->title;
    }

    public function setTitle($title)
    {
        $this->title = $title;
    }

    public function getBody()
    {
        return $this->body;
    }

    public function setBody($body)
    {
        $this->body = $body;
    }
}